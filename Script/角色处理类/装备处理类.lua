function 装备处理类:装备修理处理(连接id,序号,id,内容)
  local 格子1=玩家数据[id].角色.数据.道具[内容.序列]
  local 格子2=玩家数据[id].角色.数据.道具[内容.序列1]
  if 玩家数据[id].道具.数据[格子1]==nil then
    道具刷新(id)
    return
  end
  if 玩家数据[id].道具.数据[格子1].总类 ~=2 then
    常规提示(id,"#Y/只有装备才可以进行修理")
    return
  end
  if 玩家数据[id].道具.数据[格子1].耐久 >=400 then
    常规提示(id,"#Y/目前该装备无需修理")
    return
  end
  if 玩家数据[id].道具.数据[格子1].耐久 ==nil then
    常规提示(id,"#Y/该物品无法修复")
    return
  end
  if 玩家数据[id].道具.数据[格子1].修理失败~=nil and 玩家数据[id].道具.数据[格子1].修理失败 >=3 then
    常规提示(id,"#Y/该装备已经无法修理")
    return
  end
  local 修理费用 = math.floor(玩家数据[id].道具.数据[格子1].级别限制/500*15000*300)
  if 取银子(id) < 修理费用 then
    常规提示(id,"#Y/你身上的银子不够修理该装备！")
    return
  end
  玩家数据[id].角色:扣除银子(修理费用,0,0,"修理装备耐久",1)
  local 失败几率 = 20+math.floor(玩家数据[id].角色.数据.打造熟练度/100)
  local 珍珠 = 0
  if 格子2 ~= 0 and 玩家数据[id].道具.数据[格子2] ~= nil and 玩家数据[id].道具.数据[格子2].名称 == "珍珠" then
    if 玩家数据[id].道具.数据[格子2].级别限制 < 玩家数据[id].道具.数据[格子1].级别限制 then
      常规提示(id,"#Y/该珍珠等级过低！")
      return
    else
      失败几率 = 151
      珍珠 =1
    end
    玩家数据[id].道具.数据[格子2] = nil
    玩家数据[id].角色.数据.道具[内容.序列1] = nil
  end
  if 失败几率<=取随机数(1,150) then
    if 玩家数据[id].道具.数据[格子1].修理失败==nil then
        玩家数据[id].道具.数据[格子1].修理失败=0
    end
    玩家数据[id].道具.数据[格子1].修理失败=玩家数据[id].道具.数据[格子1].修理失败+1
    常规提示(id,"#Y/你的装备在修理时出现了点意外")
  end
  if 珍珠 == 0 then
    玩家数据[id].道具.数据[格子1].耐久=500
  else
    玩家数据[id].道具.数据[格子1].耐久=玩家数据[id].道具.数据[格子1].耐久+500
  end

  常规提示(id,"#Y/装备修理成功！")
  道具刷新(id)
end

function 装备处理类:装备分解处理(连接id,序号,id,内容)
  local 格子1=玩家数据[id].角色.数据.道具[内容.序列]
  local 格子2=玩家数据[id].角色.数据.道具[内容.序列1]
  if 玩家数据[id].道具.数据[格子1]==nil or 玩家数据[id].道具.数据[格子2]==nil then
    道具刷新(id)
    return
  end
  if (玩家数据[id].道具.数据[格子1].总类==5 or 玩家数据[id].道具.数据[格子2].总类==5) and (玩家数据[id].道具.数据[格子1].总类==2 or 玩家数据[id].道具.数据[格子2].总类==2) then
    local 宝石格子=0
    local 装备格子=0
    if 玩家数据[id].道具.数据[格子1].总类==5 then
      宝石格子=格子1
      装备格子=格子2
    else
      宝石格子=格子2
      装备格子=格子1
    end
    if 玩家数据[id].道具.数据[宝石格子].分类~=3  then
      常规提示(id,"只有分解符才可以分解装备")
      return
    end
    local 装备等级=玩家数据[id].道具.数据[装备格子].级别限制
    if 装备等级<60 or 装备等级>140 then
      常规提示(id,"只有50级以上，150级以下的装备才可以进行分解")
      return
    end
    local 所需分解符数量=0
    local 吸附石数量=0
    if 装备等级==60 or 装备等级==70 or 装备等级==80 then
      所需分解符数量=5
      吸附石数量=1
      if 装备等级==60 then
        吸附石数量=1
      elseif 装备等级==70 then
        吸附石数量=取随机数(1,2)
      elseif 装备等级==80 then
        吸附石数量=取随机数(2,3)
      end
    elseif 装备等级==90 or 装备等级==100 then
      所需分解符数量=6
      if 装备等级==90 then
        吸附石数量=取随机数(2,4)
      elseif 装备等级==100 then
        吸附石数量=取随机数(3,5)
      end
    elseif 装备等级==110 then
      所需分解符数量=7
      吸附石数量=取随机数(3,5)
    elseif 装备等级==120 then
      所需分解符数量=12
      吸附石数量=取随机数(4,6)
    elseif 装备等级==130 then
      所需分解符数量=16
      吸附石数量=取随机数(5,6)
    elseif 装备等级==140 then
      所需分解符数量=23
      吸附石数量=取随机数(5,6)
    end
    if 玩家数据[id].道具.数据[宝石格子].数量<所需分解符数量 then
      常规提示(id,装备等级.."级装备分解需要"..所需分解符数量.."个分解符！")
      return
    end
    if 宝石格子==玩家数据[id].角色.数据.道具[内容.序列] then
      if 玩家数据[id].道具.数据[宝石格子].数量-所需分解符数量>=1 then
        玩家数据[id].角色.数据.道具[内容.序列1]=nil
        玩家数据[id].道具.数据[宝石格子].数量=玩家数据[id].道具.数据[宝石格子].数量-所需分解符数量
        玩家数据[id].道具.数据[装备格子]=nil
      else
        玩家数据[id].角色.数据.道具[内容.序列]=nil
        玩家数据[id].角色.数据.道具[内容.序列1]=nil
        玩家数据[id].道具.数据[宝石格子]=nil
        玩家数据[id].道具.数据[装备格子]=nil
      end
    else
      if 玩家数据[id].道具.数据[宝石格子].数量-所需分解符数量>=1 then
        玩家数据[id].角色.数据.道具[内容.序列]=nil
        玩家数据[id].道具.数据[宝石格子].数量=玩家数据[id].道具.数据[宝石格子].数量-所需分解符数量
        玩家数据[id].道具.数据[装备格子]=nil
      else
        玩家数据[id].角色.数据.道具[内容.序列]=nil
        玩家数据[id].角色.数据.道具[内容.序列1]=nil
        玩家数据[id].道具.数据[宝石格子]=nil
        玩家数据[id].道具.数据[装备格子]=nil
      end
    end
    for i=1,吸附石数量 do
      玩家数据[id].道具:给予道具(id,"吸附石",1)
    end
    常规提示(id,装备等级.."级装备分解成功，获得了"..吸附石数量.."个吸附石！")
    道具刷新(id)
  end
end

