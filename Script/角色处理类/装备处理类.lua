      常规提示(id,"你似乎并没有这样的道具")
      return 0
    elseif 玩家数据[id].道具.数据[self.临时id2]==nil then
      常规提示(id,"你似乎并没有这样的道具")
      return 0
    elseif 玩家数据[id].道具.数据[self.临时id2].名称 ~= "附魔宝珠" then
      常规提示(id,"请不要移动附魔宝珠的位置！")
      return 0
    end
    if 玩家数据[id].道具.数据[self.临时id1].分类>=7 then
      常规提示(id,"该物品无法开孔")
      return 0
    end
    local 套装类型={"附加状态","追加法术"}
    套装类型=套装类型[取随机数(1,#套装类型)]
