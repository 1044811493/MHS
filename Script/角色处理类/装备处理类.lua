      附加状态={"金刚护法","金刚护体","生命之泉","炼气化神","普渡众生","定心术","碎星诀","变身"},
      追加法术={"横扫千军","善恶有报","惊心一剑","壁垒击破","满天花雨","浪涌","唧唧歪歪","五雷咒","龙卷雨击"}
    }
    玩家数据[id].道具.数据[self.临时id1].套装效果={套装类型,套装效果[套装类型][取随机数(1,#套装效果[套装类型])]}
