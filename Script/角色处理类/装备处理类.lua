          玩家数据[id].道具.数据[装备格子].气血 = (玩家数据[id].道具.数据[装备格子].气血 or 0) + floor(10*(锻造级别-精炼失败等级))
          玩家数据[id].道具.数据[装备格子].气血 = (玩家数据[id].道具.数据[装备格子].气血 or 0) - floor(10*锻造级别)
        elseif 玩家数据[id].道具.数据[装备格子].分类 == 6 then
          玩家数据[id].道具.数据[装备格子].速度 = (玩家数据[id].道具.数据[装备格子].速度 or 0) + floor(5*(锻造级别-精炼失败等级))
          玩家数据[id].道具.数据[装备格子].速度 = (玩家数据[id].道具.数据[装备格子].速度 or 0) - floor(5*锻造级别)
        end
        常规提示(id,"精炼装备失败，精炼等级降低"..精炼失败等级.."级")
      end
    end
    玩家数据[id].道具.数据[装备格子].精炼等级=锻造级别
    if 宝石格子==玩家数据[id].角色.数据.道具[内容.序列] then
      玩家数据[id].角色.数据.道具[内容.序列]=nil
      玩家数据[id].道具.数据[宝石格子]=nil
    else
      玩家数据[id].角色.数据.道具[内容.序列1]=nil
      玩家数据[id].道具.数据[宝石格子]=nil
    end
    道具刷新(id)
  end
end

function 装备处理类:宝石镶嵌处理(连接id,序号,id,内容)
  local 格子1=玩家数据[id].角色.数据.道具[内容.序列]
  local 格子2=玩家数据[id].角色.数据.道具[内容.序列1]
  if 玩家数据[id].道具.数据[格子1]==nil or 玩家数据[id].道具.数据[格子2]==nil then
    道具刷新(id)
    return
  end
  --==========================================镶嵌装备===========================================
  if (玩家数据[id].道具.数据[格子1].总类==5 or 玩家数据[id].道具.数据[格子2].总类==5) and (玩家数据[id].道具.数据[格子1].总类==2 or 玩家数据[id].道具.数据[格子2].总类==2) then
    local 宝石格子=0
    local 装备格子=0
    if 玩家数据[id].道具.数据[格子1].总类==5 then
      宝石格子=格子1
      装备格子=格子2
    else
      宝石格子=格子2
      装备格子=格子1
    end
    if 玩家数据[id].道具.数据[宝石格子].分类~=6 and 玩家数据[id].道具.数据[宝石格子].分类~=8 then
      常规提示(id,"只有宝石和点化石才可以镶嵌在装备上")
      return
    end
    if 玩家数据[id].道具.数据[宝石格子].名称=="星辉石" and 玩家数据[id].道具.数据[装备格子].灵饰 then  --处理灵饰
      if 玩家数据[id].道具.数据[装备格子].级别限制 == nil then
        玩家数据[id].道具.数据[装备格子].级别限制 = 取物品数据(玩家数据[id].道具.数据[装备格子].名称)[5]
      end
      if 玩家数据[id].道具.数据[宝石格子].级别限制-1~=玩家数据[id].道具.数据[装备格子].幻化等级 then
        常规提示(id,"该灵饰目前只能用#R/"..(玩家数据[id].道具.数据[装备格子].幻化等级+1).."#Y/级的星辉石进行强化")
        return
      elseif (玩家数据[id].道具.数据[装备格子].特效==nil or 玩家数据[id].道具.数据[装备格子].特效~="无级别限制") and 玩家数据[id].道具.数据[装备格子].幻化等级>=(玩家数据[id].道具.数据[装备格子].级别限制/10) then
        常规提示(id,"#Y/灵饰强化等级不能超过装备等级")
        return
      end
      玩家数据[id].道具.数据[装备格子].幻化等级=玩家数据[id].道具.数据[装备格子].幻化等级+1
      for n=1,#玩家数据[id].道具.数据[装备格子].幻化属性.附加 do
        玩家数据[id].道具.数据[装备格子].幻化属性.附加[n].强化=0
        玩家数据[id].道具.数据[装备格子].幻化属性.附加[n].强化=math.floor(幻化属性强化[玩家数据[id].道具.数据[装备格子].幻化属性.附加[n].类型]*玩家数据[id].道具.数据[装备格子].幻化等级)
      end
      常规提示(id,"#Y/灵饰强化成功！")
      if 宝石格子==玩家数据[id].角色.数据.道具[内容.序列] then
        玩家数据[id].角色.数据.道具[内容.序列]=nil
        玩家数据[id].道具.数据[宝石格子]=nil
      else
        玩家数据[id].角色.数据.道具[内容.序列1]=nil
        玩家数据[id].道具.数据[宝石格子]=nil
      end
      道具刷新(id)
      return
    end
    if 玩家数据[id].道具.数据[宝石格子].名称=="钟灵石" and 玩家数据[id].道具.数据[装备格子].灵饰 and 钟灵石定制 then  --处理灵饰
      if 玩家数据[id].道具.数据[装备格子].级别限制 == nil then
        玩家数据[id].道具.数据[装备格子].级别限制 = 取物品数据(玩家数据[id].道具.数据[装备格子].名称)[5]
      end
      if 玩家数据[id].道具.数据[装备格子].附加特性 == nil then
        玩家数据[id].道具.数据[装备格子].附加特性 = {}
      end
      if 玩家数据[id].道具.数据[装备格子].附加特性.幻化等级 ~= nil and 玩家数据[id].道具.数据[宝石格子].级别限制 <= 玩家数据[id].道具.数据[装备格子].附加特性.幻化等级 then
        常规提示(id,"#Y/该灵饰当前需要使用#R"..(玩家数据[id].道具.数据[装备格子].附加特性.幻化等级+1).."#Y等级钟灵石进行幻化操作。")
        return
      elseif  玩家数据[id].道具.数据[装备格子].附加特性.幻化类型 ~= nil and  玩家数据[id].道具.数据[宝石格子].附加特性 ~= 玩家数据[id].道具.数据[装备格子].附加特性.幻化类型 then
        常规提示(id,"#Y/该灵饰当前需要使用#R"..玩家数据[id].道具.数据[装备格子].附加特性.幻化类型.."#Y钟灵石进行幻化操作。")
        return
      end
      local 幻化成功 = false
      if 玩家数据[id].道具.数据[装备格子].附加特性.幻化等级 == nil then
        玩家数据[id].道具.数据[装备格子].附加特性.幻化等级 = 1
        玩家数据[id].道具.数据[装备格子].附加特性.幻化类型 = 玩家数据[id].道具.数据[宝石格子].附加特性
        幻化成功 = true
      else
        玩家数据[id].道具.数据[装备格子].附加特性.幻化等级 = 玩家数据[id].道具.数据[装备格子].附加特性.幻化等级 + 1
        幻化成功 = true
      end
      if 幻化成功 then
        if 宝石格子==玩家数据[id].角色.数据.道具[内容.序列] then
          玩家数据[id].角色.数据.道具[内容.序列]=nil
          玩家数据[id].道具.数据[宝石格子]=nil
        else
          玩家数据[id].角色.数据.道具[内容.序列1]=nil
          玩家数据[id].道具.数据[宝石格子]=nil
        end
        常规提示(id,"#Y/灵饰幻化特性成功！")
        道具刷新(id)
      end
      return
    end
    if 玩家数据[id].道具.数据[宝石格子].名称=="点化石" and (玩家数据[id].道具.数据[装备格子].分类==7 or 玩家数据[id].道具.数据[装备格子].分类==8 or 玩家数据[id].道具.数据[装备格子].分类==9) then  --召唤兽套装点化处理
      玩家数据[id].道具.数据[装备格子].套装效果={"附加技能",玩家数据[id].道具.数据[宝石格子].附带技能}
      if 宝石格子==玩家数据[id].角色.数据.道具[内容.序列] then
        玩家数据[id].角色.数据.道具[内容.序列]=nil
        玩家数据[id].道具.数据[宝石格子]=nil
      else
        玩家数据[id].角色.数据.道具[内容.序列1]=nil
        玩家数据[id].道具.数据[宝石格子]=nil
      end
      常规提示(id,"#Y/召唤兽装备点化成功！")
      道具刷新(id)
      return
    end
    if 玩家数据[id].道具.数据[宝石格子].名称=="精魄灵石" and (玩家数据[id].道具.数据[装备格子].分类==7 or 玩家数据[id].道具.数据[装备格子].分类==8 or 玩家数据[id].道具.数据[装备格子].分类==9) then  --召唤兽套装点化处理
      local 锻造级别=0
      if 玩家数据[id].道具.数据[装备格子].锻造级别~=nil then
        锻造级别=玩家数据[id].道具.数据[装备格子].锻造级别
      end
      锻造级别=锻造级别+1
      if 玩家数据[id].道具.数据[宝石格子].级别限制~=锻造级别 then
        常规提示(id,"该装备目前只能用#R/"..锻造级别.."#Y/级精魄灵石镶嵌")
        return
      end
      if 玩家数据[id].道具.数据[装备格子].镶嵌宝石==nil then
        玩家数据[id].道具.数据[装备格子].镶嵌宝石={}
      end
      if 玩家数据[id].道具.数据[装备格子].镶嵌宝石[1]==nil then
        玩家数据[id].道具.数据[装备格子].镶嵌宝石[1]=玩家数据[id].道具.数据[宝石格子].特效
      elseif 玩家数据[id].道具.数据[装备格子].镶嵌宝石[1]~=玩家数据[id].道具.数据[宝石格子].特效  then
        常规提示(id,"召唤兽装备最多只能镶嵌一种不同类型的精魄灵石")
        return
      end
      玩家数据[id].道具.数据[装备格子].锻造级别=锻造级别
      if 玩家数据[id].道具.数据[宝石格子].特效 == "气血" then
        玩家数据[id].道具.数据[装备格子].气血 = (玩家数据[id].道具.数据[装备格子].气血 or 0) - floor((玩家数据[id].道具.数据[宝石格子].级别限制-1))
        玩家数据[id].道具.数据[装备格子].气血 = (玩家数据[id].道具.数据[装备格子].气血 or 0) + floor(玩家数据[id].道具.数据[宝石格子].级别限制)
      elseif 玩家数据[id].道具.数据[宝石格子].特效 == "防御" then
        玩家数据[id].道具.数据[装备格子].防御 = (玩家数据[id].道具.数据[装备格子].防御 or 0) - floor((玩家数据[id].道具.数据[宝石格子].级别限制-1))
        玩家数据[id].道具.数据[装备格子].防御 = (玩家数据[id].道具.数据[装备格子].防御 or 0) + floor(玩家数据[id].道具.数据[宝石格子].级别限制)
      elseif 玩家数据[id].道具.数据[宝石格子].特效 == "伤害" then
        玩家数据[id].道具.数据[装备格子].伤害 = (玩家数据[id].道具.数据[装备格子].伤害 or 0) - floor((玩家数据[id].道具.数据[宝石格子].级别限制-1))
        玩家数据[id].道具.数据[装备格子].伤害 = (玩家数据[id].道具.数据[装备格子].伤害 or 0) + floor(玩家数据[id].道具.数据[宝石格子].级别限制)
      elseif 玩家数据[id].道具.数据[宝石格子].特效 == "灵力" then
        玩家数据[id].道具.数据[装备格子].灵力 = (玩家数据[id].道具.数据[装备格子].灵力 or 0) - floor((玩家数据[id].道具.数据[宝石格子].级别限制-1))
        玩家数据[id].道具.数据[装备格子].灵力 = (玩家数据[id].道具.数据[装备格子].灵力 or 0) + floor(玩家数据[id].道具.数据[宝石格子].级别限制)
      elseif 玩家数据[id].道具.数据[宝石格子].特效 == "命中" then
        玩家数据[id].道具.数据[装备格子].命中 = (玩家数据[id].道具.数据[装备格子].命中 or 0) - floor((玩家数据[id].道具.数据[宝石格子].级别限制-1))
        玩家数据[id].道具.数据[装备格子].命中 = (玩家数据[id].道具.数据[装备格子].命中 or 0) + floor(玩家数据[id].道具.数据[宝石格子].级别限制)
      elseif 玩家数据[id].道具.数据[宝石格子].特效 == "速度" then
        玩家数据[id].道具.数据[装备格子].速度 = (玩家数据[id].道具.数据[装备格子].速度 or 0) - floor((玩家数据[id].道具.数据[宝石格子].级别限制-1))
        玩家数据[id].道具.数据[装备格子].速度 = (玩家数据[id].道具.数据[装备格子].速度 or 0) + floor(玩家数据[id].道具.数据[宝石格子].级别限制)
      elseif 玩家数据[id].道具.数据[宝石格子].特效 == "躲避" then
        玩家数据[id].道具.数据[装备格子].躲避 = (玩家数据[id].道具.数据[装备格子].躲避 or 0) - floor((玩家数据[id].道具.数据[宝石格子].级别限制-1))
        玩家数据[id].道具.数据[装备格子].躲避 = (玩家数据[id].道具.数据[装备格子].躲避 or 0) + floor(玩家数据[id].道具.数据[宝石格子].级别限制)
      end
      if 宝石格子==玩家数据[id].角色.数据.道具[内容.序列] then
        玩家数据[id].角色.数据.道具[内容.序列]=nil
        玩家数据[id].道具.数据[宝石格子]=nil
      else
        玩家数据[id].角色.数据.道具[内容.序列1]=nil
        玩家数据[id].道具.数据[宝石格子]=nil
      end
      常规提示(id,"#Y/召唤兽装备强化成功！")
      道具刷新(id)
      return
    end
    if self:取可以镶嵌(玩家数据[id].道具.数据[装备格子].分类,玩家数据[id].道具.数据[宝石格子].子类) then
      local 锻造级别=0
      if 玩家数据[id].道具.数据[装备格子].锻炼等级~=nil then
        锻造级别=玩家数据[id].道具.数据[装备格子].锻炼等级
      end
      锻造级别=锻造级别+1
      if 玩家数据[id].道具.数据[宝石格子].级别限制~=锻造级别 then
        常规提示(id,"该装备目前只能用#R/"..锻造级别.."#Y/级宝石镶嵌")
        return
      end
      if 玩家数据[id].道具.数据[装备格子].镶嵌宝石==nil then
