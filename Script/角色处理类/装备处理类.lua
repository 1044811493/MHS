          玩家数据[id].角色.数据.体力=玩家数据[id].角色.数据.体力-self.消耗体力
          常规提示(id,"你消耗了:"..self.扣除金钱.."金钱"..self.消耗体力.."体力")
          常规提示(id,"装备开孔成功")
        else
          玩家数据[id].角色:扣除银子(self.扣除金钱,0,0,"符石开孔",1)
          玩家数据[id].角色.数据.体力=玩家数据[id].角色.数据.体力-self.消耗体力
          常规提示(id,"你消耗了:"..self.扣除金钱.."金钱"..self.消耗体力.."体力")
          常规提示(id,"装备开孔失败")

        end
        self.发送信息 = {}
        self.发送信息.体力=玩家数据[id].角色.数据.体力
