    玩家数据[id].道具.数据[临时id].灵饰=true
    玩家数据[id].道具.数据[临时id].耐久=500
    玩家数据[id].道具.数据[临时id].特效=无级别
    if 专用~=nil then
      玩家数据[id].道具.数据[临时id].专用=id
      玩家数据[id].道具.数据[临时id].不可交易=true
    end
    玩家数据[id].角色.数据.道具[临时格子]=临时id
    常规提示(id,"#Y/你得到了#R/"..名称)
  end
end

