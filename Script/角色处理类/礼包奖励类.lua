function 礼包奖励类:全套灵饰(id,等级,无级别,专用)
  for n=1,#随机灵饰 do
    local 名称=制造装备[随机灵饰[n]][等级]
    local 临时id=玩家数据[id].道具:取新编号()
    local 临时格子=玩家数据[id].角色:取道具格子()
    local 道具 = 物品类()
